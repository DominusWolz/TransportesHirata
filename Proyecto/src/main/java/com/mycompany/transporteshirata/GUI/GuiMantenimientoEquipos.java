    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int fila = jTable1.getSelectedRow();
        if (fila != -1) {
            Object idObj = jTable1.getValueAt(fila, 0);
            jTextField1.setText(idObj != null ? idObj.toString() : "");

            Object fechaObj = jTable1.getValueAt(fila, 1);
            txt_fechaequipo.setText(fechaObj != null ? fechaObj.toString() : "");

            Object tipoObj = jTable1.getValueAt(fila, 2);
            if (tipoObj != null) {
                cmb_tipomanequipo.setSelectedItem(tipoObj.toString());
            }

            Object descObj = jTable1.getValueAt(fila, 3);
            txt_descripcionman.setText(descObj != null ? descObj.toString() : "");

            Object obsObj = jTable1.getValueAt(fila, 4);
            txt_observaciones.setText(obsObj != null ? obsObj.toString() : "");

            Object equipoObj = jTable1.getValueAt(fila, 5);
            if (equipoObj != null) {
                String nombre = equipoObj.toString();
                for (int i = 0; i < cmb_equipos.getItemCount(); i++) {
                    if (cmb_equipos.getItemAt(i).equals(nombre)) {
                        cmb_equipos.setSelectedIndex(i);
                        break;
                    }
                }
            }

            // sincronizar cmb_estado con el estado del equipo mostrado en la tabla
            Object estadoObj = jTable1.getValueAt(fila, 6);
            if (estadoObj != null) {
                String estado = estadoObj.toString();
                if (estado != null && cmb_estado.getItemCount() > 0) {
                    cmb_estado.setSelectedItem(estado);
                }
            }

            Object piezaObj = jTable1.getColumnCount() > 7 ? jTable1.getValueAt(fila, 7) : null;
            if (piezaObj != null) {
                String nombrePieza = piezaObj.toString();
                for (int i = 0; i < cmb_piezas.getItemCount(); i++) {
                    PiezaInventario pieza = cmb_piezas.getItemAt(i);
                    if (pieza != null && nombrePieza.equals(pieza.getNombre())) {
                        cmb_piezas.setSelectedIndex(i);
                        break;
                    }
                }
            }
            Object cantidadPiezaObj = jTable1.getColumnCount() > 8 ? jTable1.getValueAt(fila, 8) : null;
            txt_cantidadpieza.setText(cantidadPiezaObj != null ? cantidadPiezaObj.toString() : "");

            cambiarAModoEdicion();
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void bt_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarActionPerformed
        if (txt_fechaequipo.getText().trim().isEmpty() ||
        txt_descripcionman.getText().trim().isEmpty() ||
        cmb_tipomanequipo.getSelectedItem() == null ||
        cmb_equipos.getSelectedItem() == null ||
        cmb_estado.getSelectedItem() == null) {
        
        JOptionPane.showMessageDialog(this, "⚠️ Debes completar todos los campos antes de guardar");
        return;
    }
// Verificamos que haya un ID seleccionado
        String idText = jTextField1.getText().trim();
        if (idText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione un mantenimiento de la tabla para editar.");
            return;
        }

        try {
            int id = Integer.parseInt(idText);

            MantenimientoEquipoOficina m = new MantenimientoEquipoOficina();
            m.setIdMantenimiento(id);

            if (txt_fechaequipo.getText().trim().isEmpty()) {
                m.setFecha(LocalDate.now());
            } else {
                try {
                    m.setFecha(LocalDate.parse(txt_fechaequipo.getText().trim()));
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use YYYY-MM-DD.");
                    return;
                }
            }

            m.setTipo(cmb_tipomanequipo.getSelectedItem().toString());
            m.setDescripcion(txt_descripcionman.getText());
            m.setObservaciones(txt_observaciones.getText());

            EquipoOficina equipoSeleccionado = null;
            String nombreSel = (String) cmb_equipos.getSelectedItem();
            List<EquipoOficina> lista = ed.listarEquipos();
            if (lista != null) {
                for (EquipoOficina e : lista) {
                    if (nombreSel.equals(e.getNombre())) {
                        equipoSeleccionado = e;
                        break;
                    }
                }
            }
            if (equipoSeleccionado == null) {
                JOptionPane.showMessageDialog(this, "No se pudo determinar el equipo. Seleccione uno válido.");
                return;
            }

            // sincronizar estado seleccionado en cmb_estado con la tabla EquipoOficina
            if (cmb_estado.getSelectedItem() != null) {
                String nuevoEstado = cmb_estado.getSelectedItem().toString();
                equipoSeleccionado.setEstado(nuevoEstado);
                ed.modificarEquipo(equipoSeleccionado);
            }

            m.setEquipo(equipoSeleccionado);

            boolean ok = mantDao.modificarMantenimiento(m);
            if (ok) {
                JOptionPane.showMessageDialog(this, "✅ Mantenimiento editado correctamente.");
                limpiarFormulario();
                cargarTablaMantenimientos();
                cargarComboEquipos();
                cargarComboPiezas();
            } else {
                JOptionPane.showMessageDialog(this, "❌ No se pudo editar el mantenimiento.");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID inválido.");
        }
    }//GEN-LAST:event_bt_editarActionPerformed

