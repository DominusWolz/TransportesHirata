    private void bt_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registrarActionPerformed
        if (txt_fechaequipo.getText().trim().isEmpty() ||
        txt_descripcionman.getText().trim().isEmpty() ||
        cmb_tipomanequipo.getSelectedItem() == null ||
        cmb_equipos.getSelectedItem() == null ||
        cmb_estado.getSelectedItem() == null) {
        
        JOptionPane.showMessageDialog(this, "⚠️ Debes completar todos los campos antes de guardar");
        return;
    }
        int fila = jTable1.getSelectedRow();

        if (cmb_equipos.getItemCount() == 0 || cmb_equipos.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un equipo en el combo.");
            return;
        }

        try {
            MantenimientoEquipoOficina m = new MantenimientoEquipoOficina();

            if (txt_fechaequipo.getText().trim().isEmpty()) {
                m.setFecha(LocalDate.now());
            } else {
                try {
                    m.setFecha(LocalDate.parse(txt_fechaequipo.getText().trim()));
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use YYYY-MM-DD.");
                    return;
                }
            }

            m.setTipo(cmb_tipomanequipo.getSelectedItem().toString());
            m.setDescripcion(txt_descripcionman.getText());
            m.setObservaciones(txt_observaciones.getText());
            PiezaInventario piezaSeleccionada = (PiezaInventario) cmb_piezas.getSelectedItem();
            if (piezaSeleccionada != null && piezaSeleccionada.getIdPieza() > 0) {
                if (txt_cantidadpieza.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Ingrese la cantidad de repuestos usados.");
                    return;
                }
                int cantidadUsada = Integer.parseInt(txt_cantidadpieza.getText().trim());
                if (cantidadUsada <= 0) {
                    JOptionPane.showMessageDialog(this, "La cantidad de repuestos debe ser mayor a 0.");
                    return;
                }
                m.setPiezaUsada(piezaSeleccionada);
                m.setCantidadPiezaUsada(cantidadUsada);
            }

            // Equipo: buscar por nombre
            EquipoOficina equipoSeleccionado = null;
            String nombreSel = (String) cmb_equipos.getSelectedItem();
            if (nombreSel != null) {
                List<EquipoOficina> lista = ed.listarEquipos();
                if (lista != null) {
                    for (EquipoOficina e : lista) {
                        if (nombreSel.equals(e.getNombre())) {
                            equipoSeleccionado = e;
                            break;
                        }
                    }
                }
            }

            if (equipoSeleccionado == null) {
                JOptionPane.showMessageDialog(this, "No se pudo determinar el equipo. Seleccione uno válido.");
                return;
            }

            m.setEquipo(equipoSeleccionado);

            // Sincronizar estado seleccionado en cmb_estado con la tabla EquipoOficina
            if (cmb_estado.getSelectedItem() != null) {
                String nuevoEstado = cmb_estado.getSelectedItem().toString();
                equipoSeleccionado.setEstado(nuevoEstado);
                ed.modificarEquipo(equipoSeleccionado);
            }

            String idText = jTextField1.getText().trim();
            if (idText.isEmpty()) {
                boolean ok = mantDao.registrarMantenimiento(m);
                if (ok) {
                    JOptionPane.showMessageDialog(this, "✅ Mantenimiento registrado correctamente.");
                    limpiarFormulario();
                    cargarTablaMantenimientos();
                    cargarComboEquipos(); // recargo combo para reflejar estado actualizado
                    cargarComboPiezas();
                }
            } else {
                try {
                    int id = Integer.parseInt(idText);
                    m.setIdMantenimiento(id);

                    boolean ok = mantDao.modificarMantenimiento(m);
                    if (ok) {
                        JOptionPane.showMessageDialog(this, "✅ Mantenimiento modificado correctamente.");
                        limpiarFormulario();
                        cargarTablaMantenimientos();
                        cargarComboEquipos(); // recargo combo para reflejar estado actualizado
                        cargarComboPiezas();
                    }
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "ID inválido.");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }
    public void cargarComboEquipos() {
    cmb_equipos.removeAllItems();
    List<EquipoOficina> lista = ed.listarEquipos();
    System.out.println("DEBUG cargarComboEquipos lista=" + lista);
    if (lista == null || lista.isEmpty()) {
        return;
    }
    for (EquipoOficina e : lista) {
        // añadimos solo el nombre (String) para evitar problemas de tipos
        cmb_equipos.addItem(e.getNombre());
    }
    if (cmb_equipos.getItemCount() > 0) {
        cmb_equipos.setSelectedIndex(0);
    }

}
    public void cargarComboPiezas() {
        cmb_piezas.removeAllItems();
        PiezaInventario sinPieza = new PiezaInventario();
        sinPieza.setIdPieza(0);
        sinPieza.setNombre("Sin repuesto");
        cmb_piezas.addItem(sinPieza);

        for (PiezaInventario pieza : piezaDao.listarPiezas()) {
            cmb_piezas.addItem(pieza);
        }
        if (cmb_piezas.getItemCount() > 0) {
            cmb_piezas.setSelectedIndex(0);
        }
    }

    private void cargarTablaMantenimientos() {
    String col[] = {"id", "Fecha", "Tipo", "Descripcion", "Observaciones", "Equipo", "Estado", "Repuesto usado", "Cant."};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0);
        List<MantenimientoEquipoOficina> lista = mantDao.listarMantenimientos();
        if (lista != null) {
            for (MantenimientoEquipoOficina m : lista) {
                Object[] objs = {
                    m.getIdMantenimiento(),
                    m.getFecha() != null ? m.getFecha().toString() : "",
                    m.getTipo(),
                    m.getDescripcion(),
                    m.getObservaciones(),
                    m.getEquipo() != null ? m.getEquipo().getNombre() : "",
                    m.getEquipo() != null ? m.getEquipo().getEstado() : "",
                    m.getPiezaUsada() != null ? m.getPiezaUsada().getNombre() : "Sin repuesto",
                    m.getCantidadPiezaUsada() > 0 ? m.getCantidadPiezaUsada() : ""
                };
                tableModel.addRow(objs);
            }
        }
        jTable1.setModel(tableModel);
    }//GEN-LAST:event_bt_registrarActionPerformed

