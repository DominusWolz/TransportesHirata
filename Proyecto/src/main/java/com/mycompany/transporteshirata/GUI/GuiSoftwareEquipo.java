    private void tbl_software_swMouseClicked(java.awt.event.MouseEvent evt) {                                             
        int fila = tbl_software_sw.getSelectedRow();
        if (fila == -1) return;

        txt_id_sw.setText(tbl_software_sw.getValueAt(fila, 0).toString());

        Object equipoObj = tbl_software_sw.getValueAt(fila, 1);
        if (equipoObj != null) {
            for (int i = 0; i < cmb_equipo_sw.getItemCount(); i++) {
                if (cmb_equipo_sw.getItemAt(i).equals(equipoObj.toString())) {
                    cmb_equipo_sw.setSelectedIndex(i);
                    break;
                }
            }
        }
        
        txt_nombreSoftware_sw.setText(tbl_software_sw.getValueAt(fila, 2).toString());
        txt_version_sw.setText(tbl_software_sw.getValueAt(fila, 3).toString());
        
        
        // columna 4 = F. Instalacion, columna 5 = F. Actualizacion
        Object fi = tbl_software_sw.getValueAt(fila, 4);
        txt_fechaInstalacion_sw.setText(fi != null ? fi.toString() : "");

        Object fa = tbl_software_sw.getValueAt(fila, 5);
        txt_fechaActualizacion_sw.setText(
            fa != null && !fa.toString().equals("Sin actualizar") ? fa.toString() : "");

        Object obs = tbl_software_sw.getValueAt(fila, 6);
        txt_observaciones_sw.setText(obs != null ? obs.toString() : "");

        cambiarAModoEdicion();
        // TODO add your handling code here:
    }                                            

    private void bt_guardar_swActionPerformed(java.awt.event.ActionEvent evt) {                                              
        if (cmb_equipo_sw.getItemCount() == 0 || cmb_equipo_sw.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un equipo.");
            return;
        }
        if (txt_nombreSoftware_sw.getText().trim().isEmpty() || txt_version_sw.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre del software y la version son obligatorios.");
            return;
        }
        if (!validarFechas()) return;

        String nombreSel = (String) cmb_equipo_sw.getSelectedItem();
        EquipoOficina equipoSeleccionado = null;
        for (EquipoOficina e : ed.listarEquipos()) {
            if (nombreSel.equals(e.getNombre())) {
                equipoSeleccionado = e;
                break;
            }
        }
        if (equipoSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "No se pudo identificar el equipo seleccionado.");
            return;
        }

        SoftwareEquipo sw = new SoftwareEquipo();
        sw.setEquipo(equipoSeleccionado);
        sw.setNombreSoftware(txt_nombreSoftware_sw.getText().trim());
        sw.setVersion(txt_version_sw.getText().trim());
        sw.setFechaInstalacion(java.time.LocalDate.parse(txt_fechaInstalacion_sw.getText().trim()));
        sw.setFechaActualizacion(java.time.LocalDate.parse(txt_fechaActualizacion_sw.getText().trim()));
        sw.setObservaciones(txt_observaciones_sw.getText().trim());

        if (swDao.registrarSoftware(sw)) {
            JOptionPane.showMessageDialog(this, "Software guardado exitosamente.");
            cargarTabla();
            limpiarFormulario();
            cambiarAModoNuevo();
        }
        // TODO add your handling code here:
    }                                             

    private void bt_actualizar_swActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        if (txt_id_sw.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona un software de la tabla.");
            return;
        }
        if (txt_version_sw.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa la nueva version.");
            return;
        }
        if (!validarFechas()) return;

        SoftwareEquipo sw = new SoftwareEquipo();
        sw.setIdSoftware(Integer.parseInt(txt_id_sw.getText().trim()));
        sw.setVersion(txt_version_sw.getText().trim());
        sw.setFechaActualizacion(java.time.LocalDate.parse(txt_fechaActualizacion_sw.getText().trim()));
        sw.setObservaciones(txt_observaciones_sw.getText().trim());

        if (swDao.actualizarVersion(sw)) {
            JOptionPane.showMessageDialog(this,
                "Version actualizada correctamente.\n"
                + "Software: " + txt_nombreSoftware_sw.getText() + "\n"
                + "Nueva version: " + sw.getVersion() + "\n"
                + "Fecha: " + sw.getFechaActualizacion());
            cargarTabla();
            limpiarFormulario();
            cambiarAModoNuevo();
        }
        // TODO add your handling code here:
    }                                                

    private void bt_eliminar_swActionPerformed(java.awt.event.ActionEvent evt) {                                               
        if (txt_id_sw.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona un software de la tabla.");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this,
            "¿Eliminar el registro de \"" + txt_nombreSoftware_sw.getText() + "\"?",
            "Confirmar eliminacion", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            swDao.eliminarSoftware(Integer.parseInt(txt_id_sw.getText().trim()));
            JOptionPane.showMessageDialog(this, "Software eliminado.");
            cargarTabla();
            limpiarFormulario();
            cambiarAModoNuevo();
        }
        // TODO add your handling code here:
    }                                              

