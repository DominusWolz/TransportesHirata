    // ── Validación de fechas ──────────────────────────────────────────────────
    private boolean validarFechas() {
        try {
            java.time.LocalDate fi = java.time.LocalDate.parse(txt_fechaInstalacion_sw.getText().trim());
            java.time.LocalDate fa = java.time.LocalDate.parse(txt_fechaActualizacion_sw.getText().trim());
            if (fa.isBefore(fi)) {
                JOptionPane.showMessageDialog(this,
                        "La fecha de actualización no puede ser anterior a la de instalación.",
                        "Error de fechas", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Formato de fecha incorrecto. Usa yyyy-MM-dd (ej: 2024-03-15)",
                    "Error de fechas", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private void cargarComboEquipos() {
        cmb_equipo_sw.removeAllItems();
        List<EquipoOficina> lista = ed.listarEquipos();
        if (lista == null) {
            return;
        }
        for (EquipoOficina e : lista) {
            cmb_equipo_sw.addItem(e.getNombre());
        }
        if (cmb_equipo_sw.getItemCount() > 0) {
            cmb_equipo_sw.setSelectedIndex(0);
        }
    }

    public void cargarTabla() {
        String[] col = {"ID", "Equipo", "Software", "Version",
            "F. Instalacion", "F. Actualizacion", "Observaciones"};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0) {
            public boolean isCellEditable(int r, int c) {
                return false;
            }
        };
        List<SoftwareEquipo> lista = swDao.listarTodos();
        if (lista != null) {
            for (SoftwareEquipo sw : lista) {
                Object[] objs = {
                    sw.getIdSoftware(),
                    sw.getEquipo() != null ? sw.getEquipo().getNombre() : "",
                    sw.getNombreSoftware(),
                    sw.getVersion(),
                    sw.getFechaInstalacion() != null ? sw.getFechaInstalacion().toString() : "",
                    sw.getFechaActualizacion() != null ? sw.getFechaActualizacion().toString() : "Sin actualizar",
                    sw.getObservaciones() != null ? sw.getObservaciones() : ""
                };
                tableModel.addRow(objs);
            }
        }
        tbl_software_sw.setModel(tableModel);
    }

